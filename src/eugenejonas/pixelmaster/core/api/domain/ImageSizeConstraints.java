
package eugenejonas.pixelmaster.core.api.domain;


/**
 * Defines minimal and maximal image size that app will handle.
 */
public final class ImageSizeConstraints
{
	/**
	 * Maximal width of image that app will handle.
	 */
	public static final int MAX_IMAGE_WIDTH = 1280;

	/**
	 * Maximal height of image that app will handle.
	 */
	public static final int MAX_IMAGE_HEIGHT = 1280;

	/**
	 * Minimal width of image.
	 */
	public static final int MIN_IMAGE_WIDTH = 1;

	/**
	 * Minimal height of image.
	 */
	public static final int MIN_IMAGE_HEIGHT = 1;
}
